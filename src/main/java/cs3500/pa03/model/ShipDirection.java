package cs3500.pa03.model;

/**
 * Represents a direction a Ship may be placed on the board.
 * Horizontal = horizontally to the right
 * Vertical = vertically down.
 */
public enum ShipDirection {
  /**
   * Represents a direction a Ship may be placed on the board.
   * Horizontal = horizontally to the right
   */
  HORIZONTAL,
  /**
   * Represents a direction a Ship may be placed on the board.
   * Vertical = vertically down.
   */
  VERTICAL
}
